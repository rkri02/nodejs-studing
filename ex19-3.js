const string1 = 'Nice to meet you';
const string2 = 'Hello World';
const string3 = 'Good to see you';


const substring1 = string1.substring(0, 4);
const substring2 = string2.substring(6, 9);
const substring3 = string3.substring(8, 11);


console.log(substring1);
console.log(substring2);
console.log(substring3);


