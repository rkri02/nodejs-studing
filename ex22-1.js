setInterval(() => console.log('hello'), 1000);  // 1초에 1번씩 'hello'출력하기

const printBye = () => console.log('hi');       
setInterval(printBye, 2000);                    // 2초에 1번씩 hi출력하기

/*====================================================================
setInterval() 함수는 일정한 시간 간격으로 작업을 수행하기 위해서 사용합니다
====================================================================*/