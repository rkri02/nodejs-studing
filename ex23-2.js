const string1 = '<h1>특별가 - </h1><h2>99,000원</h2>';
const string2 = '<h1>특별가 - </h1><h2>99,000원</h2>';


const res1 = string1.replace(/<..>/g, '');
const res2 = string2.replace(/<.../g, '');


console.log(res1);
console.log(res2);

