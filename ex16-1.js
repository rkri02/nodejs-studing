let i;


for(i=1; i<=10; i++)
{
    console.log("random number %dth : %d",i, Math.random());
}

/*===================================================================
Math.random() 함수는 0 이상 1 미만의 구간에서 
근사적으로 균일한(approximately uniform) 부동소숫점 의사난수를 반환하며, 
이 값은 사용자가 원하는 범위로 변형할 수 있다. 
===================================================================*/