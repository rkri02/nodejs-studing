const numbers=[10,20,30,40,50];
console.log('numbers before',numbers);
numbers.reverse();
console.log('numbers after',numbers);


const stringArray='hello'.split('');
console.log('stringArray before ',stringArray);
stringArray.reverse();
console.log('stringArray  after ',stringArray);

/*=====================================================
reverse()는 배열의 원소의 순서를 반대로 만드는 메서드입니다. 
기존의 배열의 순서를 바꾸는것이다.
=====================================================*/