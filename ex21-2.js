const Arrays = ['바나나','사과','귤','오렌지'];


console.log(Arrays);


// shift() 함수사용
console.log(Arrays.shift());
console.log(Arrays);


// unshift() 함수사용
Arrays.unshift('토마토');
console.log(Arrays);

/*============================================
shift() 함수는 배열의 첫 번째 아이템을 제거한다.
unshift() 함수는 배열의 첫 번째 아이템을 추가한다.
============================================*/