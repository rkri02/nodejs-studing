let num = 10;

//값이 없으니 10
console.log('number : ', num);

//10을 더해줘서 값이 20
num += 10;
console.log('num += 10 : ', num);

//10을 빼줘서 값이 10
num -= 10;
console.log('num -= 10 : ', num);

//10을 곱해줘서 값이 100
num *= 10;
console.log('num *= 10 : ', num);

//10을 나누어줘서 값이 10
num /= 10;
console.log('num /= 10 : ', num);

