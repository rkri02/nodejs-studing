const part1 = ['Hello', 'everyboby'];
const part2 = ['Nice to', 'meet you'];
const part3 = ['Have a', 'good day'];


const concatStr1 = part1.concat(part2);
const concatStr2 = part1.concat(part2, part3);


console.log(concatStr1);
console.log(concatStr2);

