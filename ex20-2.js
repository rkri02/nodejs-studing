const numbers = [5,4,3,2,1];
const strings = ['e','d','c','b','a'];


const sortedNum = numbers.sort();
const sortedStr = strings.sort();


console.log('sortedNumbers:', sortedNum);
console.log('sortedNumbersDesc:', sortedNum.reverse());
console.log('sortedStrings:', sortedStr);
console.log('reverseSortDesc:', sortedStr.reverse());

/*=====================================================
위한 sort() 함수는 정렬을 위한 함수이다.
=====================================================*/