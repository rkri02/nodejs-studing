const num=1;
 

//기본적인 switch - case 문이다.
switch(num)
{
    case 1:
        console.log("예금 조회선택");break;
    case 2:
        console.log("예금 출금선택");break;
    case 3:
        console.log("예금 입금선택");break; 
    case 4:
        console.log("예금 이체선택");break;
    default:
        console.log("잘못 누르셨습니다");   
} // switch문을 닫아줌

